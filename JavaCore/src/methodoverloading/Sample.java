package methodoverloading;

public class Sample {
	//method overloading = having more than 1 method with same name, but differ
	//no. of argument
	//data of args
	//order of args
	
	public void sum(int x,int y) {}
	public void sum(float x,float y) {}
	public void sum(int x , float y) {}
	public void sum(float x, int y) {}
	public void sum(double x, double y, double z) {}
	public void sum(int x, int y, int z) {}

	
 //methods can be overloaded in same class 


}
